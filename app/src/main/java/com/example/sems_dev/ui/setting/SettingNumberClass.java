package com.example.sems_dev.ui.setting;

public class SettingNumberClass {
        private String name;
        private String number;

        public SettingNumberClass(String name, String number){
            this.name = name;
            this.number = number;
        }

        public String getName(){
            return name;
        }

        public String getNumber(){
            return  number;
        }

}
